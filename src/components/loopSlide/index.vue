<template>
    <div class="swiper-container" ref="Swiper">
        <div class="swiper-wrapper">
            <div
                class="swiper-slide"
                v-for="carouse in floor"
                :key="carouse.id"
            >
                <img v-lazy="carouse.imgUrl" />
            </div>
        </div>
        <!-- 如果需要分页器 -->
        <div class="swiper-pagination"></div>

        <!-- 如果需要导航按钮 -->
        <div class="swiper-button-prev"></div>
        <div class="swiper-button-next"></div>
    </div>
</template>

<script>
import Swiper from "swiper";
export default {
    name: "loopSlide",
    props: ["floor"],
    watch: {
        // ?????????????????????????????????????????????????????????????????????????????????????????????????????????????
        // 这里的swiper之所以能够在mounted里面生效是因为，floor中的数据是从Home拿到的，不是在这个页面请求获取，当这个页面挂载之后，说明
        // home页已经挂载了，且结构也已经形成，所以这里不需要在使用nextTick（）就能生效
        // ?????????????????????????????????????????????????????????????????????????????????????????????????????????????
        floor: {
            immediate: true,
            handler() {
                this.$nextTick(() => {
                    new Swiper(this.$refs.Swiper, {
                        // direction: 'vertical', // 垂直切换选项
                        loop: true, // 循环模式选项

                        // 如果需要分页器
                        pagination: {
                            el: ".swiper-pagination",
                        },

                        // 如果需要前进后退按钮
                        navigation: {
                            nextEl: ".swiper-button-next",
                            prevEl: ".swiper-button-prev",
                        },
                    });
                });
            },
        },
    },
};
</script>

